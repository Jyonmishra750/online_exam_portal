    //answer check
    public void answerCheck(){
        String stans = " ";
        if(jRadioButton1.isSelected()){
            stans = jRadioButton1.getText();
        }
        else if(jRadioButton2.isSelected()){
            stans = jRadioButton2.getText();
        }
        else if(jRadioButton2.isSelected()){
            stans = jRadioButton2.getText();
        }
        else if(jRadioButton3.isSelected()){
            stans = jRadioButton3.getText();
        }
        else{
            stans = jRadioButton4.getText();
        }

        if(stans.equals(answer)){
            marks += 1;
            String mark1 = String.valueOf(marks);
            jLabel18.setText(mark1);
        }

        //question number change
        int questionnumber = Integer.parseInt(questionid);
        questionnumber += 1;
        questionid = String.valueOf(questionnumber);
        
        //clear jRadioButton
        jRadioButton1.setSelected(false);
        jRadioButton2.setSelected(false);
        jRadioButton3.setSelected(false);
        jRadioButton4.setSelected(false);
        
        //At the last question the "next" button will be hidden
        if(questionid.equals("10")){
            jButton1.setVisible(false);
        }
    }

    //question
    public void question(){
        try {
            Connection cn = repository.ConnectionDB.getConnectionDB();
            Statement st = cn.createStatement();
            ResultSet rs1 = st.executeQuery("select * from question_table where qid = '"+questionid+"'");
            while(rs1.next()){
                jLabel16.setText(rs1.getString(1));
                jLabel19.setText(rs1.getString(2));
                jRadioButton1.setText(rs1.getString(3));
                jRadioButton2.setText(rs1.getString(4));
                jRadioButton3.setText(rs1.getString(5));
                jRadioButton4.setText(rs1.getString(6));
                answer = rs1.getString(7);
            }
        } catch (SQLException se) {
        }
    }

    //submit
    public void submit(){
        String rollNo = jLabel10.getText();
        answerCheck();
        try {
            Connection cn = repository.ConnectionDB.getConnectionDB();
            Statement st = cn.createStatement();
            st.executeUpdate("update user_table set marks_secured = '"+marks+"' where roll_no = '"+rollNo+"'");
            String marks1 = String.valueOf(marks);
            
            if(marks > 6){
                JOptionPane.showMessageDialog(this, "Thank you for taking this exam. \n\n You've passed the examination\n\n Your total mark is "+marks1);
                setVisible(false);
                new view.student.student_login().setVisible(true);
            }else{
                JOptionPane.showMessageDialog(this, "Thank you for taking this exam. \n\n Sorry!!! You've failed in the examination\n\n Your total mark is "+marks1);
                setVisible(false);
                new view.student.student_login().setVisible(true);
            }
            
        } catch (SQLException se) {
        }
    }
