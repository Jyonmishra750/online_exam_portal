    Timer timer;
    public student_home(String rollNo){
        initComponents();

        jLabel10.setText(rollNo);
        //date
        SimpleDateFormat dFormat = new SimpleDateFormat("dd-MM-yyyy");
        Date dt = new Date();
        jLabel8.setText(dFormat.format(dt));

        try {
            Connection cn = repository.ConnectionDB.getConnectionDB();
            Statement st = cn.createStatement();

            ResultSet rs = st.executeQuery("select * from user_table where roll_no = '"+rollNo+"'");
            while(rs.next()){
                jLabel12.setText(rs.getString(2));
            }

            ResultSet rs1 = st.executeQuery("select * from question_table where qid = '"+questionid+"'");
            while(rs1.next()){
                jLabel16.setText(rs1.getString(1));
                jLabel19.setText(rs1.getString(2));
                jRadioButton1.setText(rs1.getString(3));
                jRadioButton2.setText(rs1.getString(4));
                jRadioButton3.setText(rs1.getString(5));
                jRadioButton4.setText(rs1.getString(6));
                answer = rs1.getString(7);
            }
        } catch (SQLException se) {
        }

        //timer
        setLocationRelativeTo(this);
        timer = new Timer(1000, new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent ae) {
                jLabel7.setText(String.valueOf(second));
                jLabel6.setText(String.valueOf(minute));

                if(second == 60){
                    second = 0;
                    minute++;
                    if(minute == 10){
                        timer.stop();
                        answerCheck();
                        submit();
                    }
                }
                second++;
            }
        });
        timer.start();
    }

